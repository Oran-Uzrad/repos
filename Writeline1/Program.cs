using System;

namespace Writeline1
{
    class Program
    {
        static void Main(string[] args)
        {
            int x = 2, y = 22, z = 222;
            Console.WriteLine("{0,5} {1,5} {2,5}", x, x, x);
            Console.WriteLine("{0,5} {1,5} {2,5}", y, y, y);
            Console.WriteLine("{0,5} {1,5} {2,5}", z, z, z);
            Console.ReadLine();
        }
    }
}

